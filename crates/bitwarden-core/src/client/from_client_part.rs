//! Traits for extracting dependencies from a Client.
//!
//! This module provides:
//! - [`FromClientPart`] trait which enables uniform extraction of dependencies from a [`Client`],
//!   facilitating macro-based generation of `from_client` methods for feature clients.
//! - [`FromClient`] trait which can be derived using `#[derive(FromClient)]` from the
//!   `bitwarden_core_macro` crate to automatically implement `from_client`.

use std::sync::Arc;

use bitwarden_crypto::KeyStore;
#[cfg(feature = "internal")]
use bitwarden_state::repository::{Repository, RepositoryItem};

use super::Client;
use crate::{client::ApiConfigurations, key_management::KeySlotIds};

/// Trait for types that can be constructed from a [`Client`].
///
/// This trait is typically derived using `#[derive(FromClient)]` from
/// the `bitwarden_core_macro` crate, which generates the implementation
/// by extracting all struct fields from the Client using [`FromClientPart`].
///
/// # Example
///
/// ```ignore
/// use bitwarden_core::client::FromClient;
/// use bitwarden_core_macro::FromClient;
///
/// #[derive(FromClient)]
/// pub struct FoldersClient {
///     key_store: KeyStore<KeySlotIds>,
///     api_configurations: Arc<ApiConfigurations>,
///     repository: Option<Arc<dyn Repository<Folder>>>,
/// }
///
/// // Usage:
/// let folders_client = FoldersClient::from_client(&client);
/// ```
pub trait FromClient: Sized {
    /// Construct this type from a [`Client`] reference.
    fn from_client(client: &Client) -> Self;
}

/// Trait for extracting a part / dependency of type `T` from a [`Client`].
///
/// Each impl describes one shape of value that `#[derive(FromClient)]` knows how to pull out
/// of a `Client`: `KeyStore<KeySlotIds>`, `Arc<ApiConfigurations>`, an optional
/// `Arc<dyn Repository<T>>`, and so on. Users normally don't call this directly - they derive
/// [`FromClient`] on a struct and the derive emits the right `get_part` calls for each field.
pub trait FromClientPart<T> {
    /// Extract a value of type `T` from this [`Client`].
    fn get_part(&self) -> T;
}

/// Trait that bridges a feature client's *trait object* into the [`FromClient`] /
/// [`FromClientPart`] machinery.
///
/// Implementing this on `dyn FooTrait` (typically auto-generated by `#[client_trait(via = ...)]`)
/// makes `Arc<dyn FooTrait>` a field type that `#[derive(FromClient)]` can populate. With the
/// impl in place, a struct like:
///
/// ```ignore
/// #[derive(FromClient)]
/// struct OuterClient {
///     inner: Arc<dyn FooTrait>,
/// }
/// ```
///
/// gets wired through `OuterClient::from_client(&client)` with no hand-written bridge.
///
/// # Why this trait exists
///
/// We can't just write `impl FromClientPart<Arc<dyn FooTrait>> for Client` from a feature crate:
/// both `FromClientPart` and `Client` live in `bitwarden-core`, and `Arc<_>` isn't a
/// fundamental wrapper, so Rust's orphan rule rejects the impl. `impl FromClientShared for
/// dyn FooTrait` is fine - `dyn FooTrait` is a local type, so implementing any trait for it
/// (including foreign ones) is allowed. The blanket impl below then bridges back to
/// [`FromClientPart`].
pub trait FromClientShared {
    /// Construct a reference-counted instance of `Self` from a [`Client`].
    fn from_client_shared(client: &Client) -> Arc<Self>;
}

impl<T: ?Sized + FromClientShared> FromClientPart<Arc<T>> for Client {
    fn get_part(&self) -> Arc<T> {
        T::from_client_shared(self)
    }
}

impl FromClientPart<KeyStore<KeySlotIds>> for Client {
    fn get_part(&self) -> KeyStore<KeySlotIds> {
        self.internal.get_key_store().clone()
    }
}

impl FromClientPart<Arc<ApiConfigurations>> for Client {
    fn get_part(&self) -> Arc<ApiConfigurations> {
        self.internal.get_api_configurations()
    }
}

#[cfg(feature = "internal")]
impl<T: RepositoryItem> FromClientPart<Option<Arc<dyn Repository<T>>>> for Client {
    fn get_part(&self) -> Option<Arc<dyn Repository<T>>> {
        self.platform().state().get::<T>().ok()
    }
}
